// bit reversal for N=256 (8 bits)
static uint8_t bit_reverse_8(uint8_t x) {
    x = ((x & 0xF0) >> 4) | ((x & 0x0F) << 4);
    x = ((x & 0xCC) >> 2) | ((x & 0x33) << 2);
    x = ((x & 0xAA) >> 1) | ((x & 0x55) << 1);
    return x;
}

void fft(int32_t* re, int32_t* im) {
    // bit reversal permutation
    for (int i = 0; i < FFT_N; i++) {
        int j = bit_reverse_8((uint8_t)i);
        if (i < j) {
            int32_t tmp;
            tmp = re[i]; re[i] = re[j]; re[j] = tmp;
            tmp = im[i]; im[i] = im[j]; im[j] = tmp;
        }
    }

    // FFT stages
    for (int stage = 1; stage <= FFT_BITS; stage++) {
        int span    = 1 << (stage - 1);   // butterfly half-span
        int step    = span << 1;           // full butterfly span
        int tw_step = 128 >> (stage - 1);  // twiddle stride

        for (int k = 0; k < span; k++) {
            int32_t wr = tw_re[k * tw_step];
            int32_t wi = tw_im[k * tw_step];

            for (int i = k; i < FFT_N; i += step) {
                int j = i + span;

                // butterfly — Q1.15 multiply: (a * b) >> 15
                int32_t tr = (int32_t)(((int64_t)wr * re[j] - (int64_t)wi * im[j]) >> 15);
                int32_t ti = (int32_t)(((int64_t)wr * im[j] + (int64_t)wi * re[j]) >> 15);

                re[j] = re[i] - tr;
                im[j] = im[i] - ti;
                re[i] = re[i] + tr;
                im[i] = im[i] + ti;
            }
        }
    }
}

void fft_magnitude(int32_t* re, int32_t* im, int32_t* out) {
    for (int i = 0; i < FFT_N / 2; i++) {
        // magnitude squared — avoids sqrt, fine for display
        int64_t mag = ((int64_t)re[i] * re[i]) + ((int64_t)im[i] * im[i]);
        // shift down to fit in int32
        out[i] = (int32_t)(mag >> 15);
    }
}